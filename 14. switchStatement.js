let day = Number(prompt("Enter the day : "));

switch (day) {
    case 1:
        console.log("Friday");
        break;
    case 2:
        console.log("Saterday");
        break;
    case 3:
        console.log("Sunday");
        break;
    case 4:
        console.log("Monday");
        break;
    case 5:
        console.log("Tuesday");
        break;
    case 6:
        console.log("Wednesday");
        break;
    default:
        console.log("Thrusday");
}